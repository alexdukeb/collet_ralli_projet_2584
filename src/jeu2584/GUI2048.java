    @Override
    public void keyPressed(KeyEvent e) {
        System.out.println(e);
        int codeDeLaTouche = e.getKeyCode();
        switch (codeDeLaTouche) // Les valeurs sont contenue dans KeyEvent. Elles commencent par VK_ et finissent par le nom de la touche
        {
            case KeyEvent.VK_UP: // si la touche enfonce est celle du haut
                fonctionPourMonter();
                break;
            case KeyEvent.VK_LEFT: // si la touche enfonce est celle de gauche
                fonctionPourAllerAGauche();
                break;
            case KeyEvent.VK_RIGHT: // si la touche enfonce est celle de droite
                fonctionPourAllerADroite();
                break;
            case KeyEvent.VK_DOWN: // si la touche enfonce est celle du bas
                fonctionPourDescendre();
                break;
        }
    }
    
